def test_apply_lock_skips_provider_with_conflicting_baseurl(
    openclaw_present: dict[str, Path], monkeypatch: pytest.MonkeyPatch
) -> None:
    """F-CFG-13: pre-existing ``worthless-openai`` with a non-worthless
    baseUrl is a conflict — skip that provider, emit PROVIDER_CONFLICT,
    continue with others. Lock-core must remain unaffected.
    """
    from worthless.openclaw import integration
    from worthless.openclaw.errors import OpenclawErrorCode

    # Pre-poison: an existing worthless-openai entry pointing at the user's
    # own gateway (not our proxy) — looks like manual tweaking we mustn't
    # silently trample.
    config_path = openclaw_present["config_path"]
    config_path.write_text(
        json.dumps(
            {
                "models": {
                    "providers": {
                        "worthless-openai": {
                            "baseUrl": "https://my-own-gateway.example.com/v1",
                            "apiKey": "user-secret",
                        }
                    }
                }
            },
            indent=2,
            sort_keys=True,
        )
        + "\n",
        encoding="utf-8",
    )
    monkeypatch.chdir(openclaw_present["home"])  # avoid project-local pickup

    result = integration.apply_lock(
        planned_updates=[
            ("openai", "openai-aaaa1111", "sk-shard-a-openai"),
            ("anthropic", "anthropic-bbbb2222", "sk-shard-a-anthropic"),
        ],
    )

    # Detected, but worthless-openai skipped on conflict.
    assert result.detected is True
    assert "worthless-openai" not in result.providers_set
    assert any(name == "worthless-openai" for name, _ in result.providers_skipped)
    assert any(e.code == OpenclawErrorCode.PROVIDER_CONFLICT for e in result.events), [
        e.code for e in result.events
    ]
    # Anthropic still got written.
    assert "worthless-anthropic" in result.providers_set


def test_apply_lock_refuses_symlink_config(
    openclaw_present: dict[str, Path], tmp_path: Path, monkeypatch: pytest.MonkeyPatch
) -> None:
    """F-CFG-15: openclaw.json is a symlink → set_provider refuses + we
    emit a structured event. Must NOT raise; lock-core success unaffected.

    Phase 1's set_provider handles symlink refusal via _file_lock failure
    or atomic-write semantics. apply_lock catches OpenclawConfigError
    and emits SYMLINK_REFUSED / WRITE_FAILED / CONFIG_UNREADABLE per
    spec — the key contract is "no raise out".
    """
    from worthless.openclaw import integration

    # Replace the real config with a symlink pointing elsewhere.
    config_path = openclaw_present["config_path"]
    real_target = tmp_path / "outside-config.json"
    real_target.write_text(json.dumps({"models": {"providers": {}}}) + "\n", encoding="utf-8")
    config_path.unlink()
    config_path.symlink_to(real_target)
    monkeypatch.chdir(openclaw_present["home"])

    # Force set_provider to refuse symlinks via monkeypatch — Phase 1's
    # contract is "atomic + flock" and does follow symlinks today; the
    # safe-by-design contract for Phase 2.b is "we refuse them in apply_lock".
    from worthless.openclaw import config as config_mod
    from worthless.openclaw.config import OpenclawConfigError

    def _refusing_set_provider(*args: object, **kwargs: object) -> None:
        raise OpenclawConfigError("refusing to follow symlink")

    monkeypatch.setattr(config_mod, "set_provider", _refusing_set_provider)

    # Must not raise.
    result = integration.apply_lock(
        planned_updates=[("openai", "openai-aaaa1111", "sk-shard-a")],
    )

    assert result.detected is True
    # Provider wasn't written; event surfaced.
    assert "worthless-openai" not in result.providers_set
    assert len(result.events) >= 1


