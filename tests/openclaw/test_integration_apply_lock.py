def test_apply_lock_warns_on_hardlinked_config(
    openclaw_present: dict[str, Path], monkeypatch: pytest.MonkeyPatch
) -> None:
    """F-CFG-15 defense-in-depth: ``openclaw.json`` with ``st_nlink > 1``
    surfaces a WRITE_FAILED warning. Atomic-write semantically preserves
    the other hardlink (``os.replace`` rebinds the name to a new inode),
    so this is non-destructive — but the user almost certainly didn't
    mean to share inodes with their config, so we surface it for doctor
    to flag. No refusal; lock-core proceeds.

    Reference: security-auditor 2026-05-07 MEDIUM finding on commit 5bd33da.
    """
    from worthless.openclaw import integration

    config_path = openclaw_present["config_path"]
    # Plant a hardlink to a sibling file in the same dir (cross-fs
    # hardlinks aren't possible; same-fs is enough to drive st_nlink).
    sibling = openclaw_present["home"] / ".openclaw" / "ghost.txt"
    config_path.write_text('{"models":{"providers":{}}}\n', encoding="utf-8")
    os.link(config_path, sibling)
    monkeypatch.chdir(openclaw_present["home"])

    result = integration.apply_lock(
        planned_updates=[("openai", "openai-aaaa1111", "sk-shard-a")],
    )

    # Lock-core proceeded — provider was written.
    assert "worthless-openai" in result.providers_set
    # A warning event surfaced citing the hardlink count.
    hardlink_events = [
        e for e in result.events if e.level == "warn" and "hardlink" in e.detail.lower()
    ]
    assert hardlink_events, [(e.code, e.detail) for e in result.events]
    assert hardlink_events[0].extra is not None
    assert hardlink_events[0].extra.get("nlink") == "2"


def test_apply_lock_recreates_deleted_config(
    openclaw_present: dict[str, Path], monkeypatch: pytest.MonkeyPatch
) -> None:
    """F-CFG-23: openclaw.json deleted between detect and write → set_provider
    recreates it. Final state has provider populated; no error raised.
    """
    from worthless.openclaw import integration

    config_path = openclaw_present["config_path"]
    monkeypatch.chdir(openclaw_present["home"])

    # Delete after detect would normally have run — patch detect to return
    # the (now-vanished) config_path so apply_lock thinks it should write.
    real_detect = integration.detect

    def _detect_then_unlink() -> object:
        state = real_detect()
        # Side effect: the file disappears between detect() returning and
        # set_provider running. Simulates a daemon-kicked vanish.
        if config_path.exists():
            config_path.unlink()
        return state

    monkeypatch.setattr(integration, "detect", _detect_then_unlink)

    result = integration.apply_lock(
        planned_updates=[("openai", "openai-aaaa1111", "sk-shard-a")],
    )

    assert result.detected is True
    # set_provider's atomic-write recreates the file.
    assert config_path.exists()
    assert "worthless-openai" in result.providers_set


